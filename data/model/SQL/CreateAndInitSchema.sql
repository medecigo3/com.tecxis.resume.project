-- Create views section -------------------------------------------------

CREATE VIEW "v_Assignment" AS
SELECT "STAFF"."NAME", "LASTNAME", "PROJECT"."NAME" AS "PROJECT", "PROJECT"."VERSION" AS "PROJECT_VERSION", "ASSIGNMENT"."DESC" AS "ASSIGNMENT"
FROM "ASSIGNMENT", "PROJECT", "STAFF"
WHERE STAFF.STAFF_ID = PROJECT.STAFF_ID AND
PROJECT.NAME = ASSIGNMENT.NAME AND
PROJECT.VERSION = ASSIGNMENT.VERSION 
ORDER BY PROJECT.NAME
/

CREATE VIEW "v_Experience" AS
SELECT "STAFF"."NAME", "LASTNAME", "SERVICE"."NAME" AS "POSITION", "CLIENT"."NAME" AS "CUSTOMER", "START_DATE", "END_DATE", "CITY"."NAME" AS "CITY", "COUNTRY"."NAME" AS "COUNTRY", "SERVICE"."DESC" AS "JOB_DESC"
FROM "SERVICE", "CLIENT", "CONTRACT", "STAFF", "PROJECT", "COUNTRY", "CITY", "LOCATION"
WHERE CITY.COUNTRY_ID = COUNTRY.COUNTRY_ID AND
LOCATION.CITY_ID = CITY.CITY_ID AND 
LOCATION.COUNTRY_ID = CITY.COUNTRY_ID AND 
LOCATION.NAME = PROJECT.NAME AND
LOCATION.VERSION = PROJECT.VERSION AND
PROJECT.CLIENT_ID = CLIENT.CLIENT_ID AND
CLIENT.CLIENT_ID = CONTRACT.CLIENT_ID AND
CONTRACT.SERVICE_ID = SERVICE.SERVICE_ID AND
PROJECT.STAFF_ID = STAFF.STAFF_ID
ORDER BY CONTRACT.START_DATE
/

